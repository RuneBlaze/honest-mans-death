class StringPrinting {
    public static void main(String[] args) {
        String a = "meow";
        int i = 100;
        System.out.println(a);
        System.out.println(i + 99);
        System.out.println(a);
        i = 42;
        System.out.println(i - 99);
        System.out.println(a);
    }
}